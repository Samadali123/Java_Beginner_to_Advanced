public class Loops {
      public static void main(String[] args) {
           System.err.println("Loops In Java");
        //    for, while, do while

           for(int i=0; i<=1000; i++){
             System.out.println(i);

           }

           System.out.println();
           
        int j=1;
         while(j<=1000){
             System.out.println(j);
             j++;
         }


         System.out.println();
        int k=1;
        do { 
             System.out.println(k);
             k++;
        } while (k<=1000);
            
      }


}
